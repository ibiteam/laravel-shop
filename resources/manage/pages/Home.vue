    let newArr = new Array()
    for(let i=0;i<arrs.length;i++){
        if(arrs[i].title.indexOf(searchtools.value) > -1){ // Â¶ÇÊûúÈ°∂Á∫ßÊ†áÈ¢òÂåÖÂê´Êü•ËØ¢ÂÜÖÂÆπ
            newArr.push({...arrs[i]})
            continue;
